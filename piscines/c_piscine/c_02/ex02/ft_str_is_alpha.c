int	ft_str_is_alpha(char *str)
{
	int		i;
	char	a;

	i = 0;
	while (str[i] != '\0')
	{
		a = 'A';
		while (a <= 'z')
		{
			if (a == '[' || a == '\\' || a == ']' || a == '^'
				|| a == '_' || a == '`')
			{
				a++;
				continue ;
			}
			else if (a == str[i])
				break ;
			else if (a == 'z' && a != str[i])
				return (0);
			a++;
		}
		i++;
	}
	return (1);
}
