#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	i2;

	i = 1;
	while (i < argc)
	{
		i2 = 0;
		while (argv[i][i2] != '\0')
		{
			write (1, &argv[i][i2], 1);
			i2++;
		}
		write (1, "\n", 1);
		i++;
	}
}
