#include "vec3.h"

t_v3	*v_pointer(t_v3 v)
{
	t_v3	*vp;

	vp = malloc(sizeof(t_v3));
	vp->x = v.x;
	vp->y = v.y;
	vp->z = v.z;
	return (vp);
}
